projects.each_with_index do |project, index|
  logo = false
  background_image = false
  logo_name = ""
  background_image_name = ""
  project[:active] = true

  if project[:logo] == "true"
    logo_file_path = if (File.file?(Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-logo.jpeg")))
      Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-logo.jpeg")
    elsif (File.file?(Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-logo.svg")))
      Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-logo.svg")
    else
      Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-logo.png")
    end

    logo_name = logo_file_path.to_s.split("/")[-1]
    logo = File.open(logo_file_path)
  end

  if project[:background_image] == "true"
    background_image_file_path = if (File.file?(Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-bg.jpeg")))
      Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-bg.jpeg")
    elsif (File.file?(Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-bg.svg")))
      Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-bg.svg")
    elsif (File.file?(Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-bg.png")))
      Rails.root.join("app/assets/images/#{project[:name].downcase.gsub(" ", "")}-bg.png")
    end

    background_image_name = background_image_file_path.to_s.split("/")[-1]
    if background_image_file_path
      background_image = File.open(background_image_file_path)
    end
  end

  new_project = Project.find_or_create_by!(project.except(:logo, :background_image))
  new_project.logo.attach(io: logo, filename: logo_name)


  if background_image_file_path
    new_project.background_image.attach(io: background_image, filename: background_image_name)
  else
    puts "BLANK HEADER"
    new_project.background_image.attach(io: File.open("app/assets/images/blank-header.png"), filename: "blank-header.png")
  end
end

